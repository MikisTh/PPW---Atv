// Desenvolva uma função que receba um número como parâmetro e verifique se ele é par ou ímpar. Retorne true se for par e false se for ímpar.
function parOuImpar(numero) {
    if (numero % 2 == 0) {
        return true;
    } else {
        return false;
    }
}

